			<h2>E-visa</h2>
			<p>Vietnamese visa is a kind of travel document granted by Vietnamese competent authorities, providing permission to travel to and enter Vietnam.<br/>
			An electronic visa (E-visa) is one of visa types issued to foreigners by the Vietnamese Immigration Department via an electronic system. Vietnam E-visa is valid for a maximum of 90 days, single or multiple entry.</p>
			
			<p>Outside Vietnam, foreigners who want to enter Vietnam can personally apply for an E-visa or through the requesting agencies and organizations.</p>
			
			<p>E-visa fee is paid via an electronic payment gateway as prescribed by the Immigration Department. The fee will not be refunded if the application is refused.</p>
			
